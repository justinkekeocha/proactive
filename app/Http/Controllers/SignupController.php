    {
        //
         
      $formFields = $request->validate([
        'name' => ['required', 'min:3'],
        'email' => ['required', 'email', Rule::unique('users', 'email')],
        'password' => 'required|min:6'
     ]);
    
      //Hash passowrd
      $formFields['password'] = Hash::make($formFields['password']);
    
      //Create user
      $user = User::create($formFields);
      return response()->json([
        'status'=>'success'
      ]);
      
